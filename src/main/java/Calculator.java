public class Calculator {
    public int add(int num1, int num2){
        return num1 + num2;
    }
    public int sub(int num1, int num2){
        return num1 - num2;
    }
    public int mutiply(int num1, int num2){
        return num1 * num2;
    }
    public double divide(double num1, double num2){
        return num1 / num2;
    }

}
